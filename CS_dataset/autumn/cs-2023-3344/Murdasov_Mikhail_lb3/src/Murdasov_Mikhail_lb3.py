table = {
    'q0':{' ':[' ', 1, 'q0'], 'a':['a', 0, 'q1']},
    'q1':{' ':[' ', -1, 'q6'], 'a':['*', 0, 'q3'], 'b':['*', 0, 'q4'], 'c':['*', 0, 'q5'],'*':['*', 1, 'q1']},
    'q2':{'a':['a', 1, 'q2'], 'b':['b', 1, 'q2'], 'c':['c', 1, 'q2'],'*':['*', 1, 'q1']},
    'q3':{' ':['a', 1, 'q2'], 'a':['a', -1, 'q3'], 'b':['b', -1, 'q3'], 'c':['c', -1, 'q3'],'*':['*', -1, 'q3']},
    'q4':{' ':['b', 1, 'q2'], 'a':['a', -1, 'q4'], 'b':['b', -1, 'q4'], 'c':['c', -1, 'q4'],'*':['*', -1, 'q4']},
    'q5':{' ':['c', 1, 'q2'], 'a':['a', -1, 'q5'], 'b':['b', -1, 'q5'], 'c':['c', -1, 'q5'],'*':['*', -1, 'q5']},
    'q6':{'*':[' ', -1, 'q6'], 'a':['a', 0, 'q7'], 'b':['b', 0, 'q7'], 'c':['c', 0, 'q7']}
}

memory = list(' '*25 + input() + ' '*25)
q = 'q0'
index = 0

while q != 'q7':
    symbol, delta, state = table[q][memory[index]]
    memory[index] = symbol
    index += delta
    q = state

print(''.join(memory))