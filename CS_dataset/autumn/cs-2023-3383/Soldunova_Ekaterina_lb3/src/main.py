table = {
    'q0':{'a':['a', 1, 'q1'], 'b':['b', 1, 'q2'], 'c':['c', 1, 'q1'], ' ':['', 1, 'q0']},
    'q1':{'a':['a', 1, 'q1'], 'b':['b', 1, 'q2'], 'c':['c', 1, 'q1'], ' ':['', -1, 'q4']},
    'q2':{'a':['', 1, 'q3'], 'b':['', 1, 'q3'], 'c':['', 1, 'q3'], ' ':['', -1, 'q5']},
    'q3':{'a':['', 1, 'q6'], 'b':['', 1, 'q6'], 'c':['', 1, 'q6'], ' ':['', 0, 'q6']},
    'q4':{'a':['a', -1, 'q4'], 'c':['c', -1, 'q4'], ' ':['', 1, 'q5'], '':['', 1, 'q5']},
    'q5':{'a':['', 0, 'q6'], 'b':['', 0, 'q6'], 'c':['', 0, 'q6'], ' ':['', 0, 'q6']}
}

state = 'q0'
ind = 0
memory = list(input())

while state != 'q6':
    char, mov, new_state = table[state][memory[ind]]
    memory[ind] = char
    ind += mov
    state = new_state
    
print(''.join(memory))