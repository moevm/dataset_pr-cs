mem = list(input())
q = 'q0'
ind = 0

while q != 'q16':
    s, mov,st = table[q][mem[ind]]
    mem[ind] = s
    if mov == 'R':
        ind += 1
    elif mov == 'L':
        ind -= 1
    q=st

print(''.join(mem))
