

program = {'q1':{'a':('a', 1, 'q2'), 'b':('b', 1, 'q2'), 'c':('c', 1, 'q2'), ' ':(' ', 1, 'q1')},
           'q2':{'a':('a', 1, 'q2'), 'b':('b', 1, 'q2'), 'c':('c', 1, 'q2'), ' ':(' ', -1, 'q3')},
           'q3':{'a':(' ', 1, 'qa'), 'b':(' ', 1, 'qb'), 'c':(' ', 1, 'qc'), ' ':(' ', 1, 'q!')},
           'qa': {'a': ('a', 1, 'qa'), 'b': ('b', 1, 'qa'), 'c': ('c', 1, 'qa'), ' ': ('a', -1, 'q4'), '!': ('!', 1, 'qa')},
           'qb': {'a': ('a', 1, 'qb'), 'b': ('b', 1, 'qb'), 'c': ('c', 1, 'qb'), ' ': ('b', -1, 'q4'), '!': ('!', 1, 'qb')},
           'qc': {'a': ('a', 1, 'qc'), 'b': ('b', 1, 'qc'), 'c': ('c', 1, 'qc'), ' ': ('c', -1, 'q4'), '!': ('!', 1, 'qc')},
           'q4': {'a': ('a', -1, 'q4'), 'b': ('b', -1, 'q4'), 'c': ('c', -1, 'q4'), ' ': ('!', -1, 'q3'), '!': ('!', -1, 'q4')},
           'q!': {'a': ('a', 0, 'q0'), 'b': ('b', 0, 'q0'), 'c': ('c', 0, 'q0'), ' ': (' ', 1, 'q!'), '!': (' ', 1, 'q!')}}


tape = list(' ' + input() + 13*' ')
state = 'q1'
index = 0

while state != 'q0':
    symbol, move, state = program[state][tape[index]]
    tape[index] = symbol
    index += move



print(''.join(tape))


